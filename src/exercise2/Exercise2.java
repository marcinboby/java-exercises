package exercise2;

/**
 *  Write a Java program to print the sum of two numbers.
 * Test Data:
 * 74 + 36
 * Expected Output :
 * 110
 */
public class Exercise2 {
    public static void main(String[] args) {
        int number1 = 74;
        int number2 = 36;
        int sum;
        sum = number1 + number2;
        System.out.println("Result of adding two numbers(" + number1 + ", " + number2 + ") is: " + sum);
    }
}
